<h2>Portals</h2>
When you start, you get a bunch of souls, but also a flint and steel. You may wonder what the purpose is
(besides burning other people). In the Quiet Craft, opening a portal to the nether is serious business.
Everything I said previously about griefers never getting back to your world when you kill them, goes
out the window when you build a portal. <span id="warn">When you open a portal to the nether, anyone else
  from any other world who also built a portal, can now travel through the nether and get to your world.</span>
This includes any griefers you killed. 
<p>So don't build a portal unless you are ready for what comes through it.</p>
<p>That said, the nether can be a very profitable place if you are ready for it</p>
<h2>The Nether</h2>
<p>There are a few important things to keep in mind in the nether.</p>
<ol><li id="warn">If you die in the nether, you will teleport out of your world into a new one, no matter
    how many souls you own.</li>
  <p>All of your souls that died with will be dropped with the rest of your items, as you go off to enjoy your new life.
    Conversely, if you kill someone in the nether, you *get* all their souls.</p> 
  <li id="warn">Going into the nether is the only way to get back to your old world after dying in it</li>
  <p>If you really want to get back to your world, you'll need your friends help. Here are the steps:</p>
  <ol>
    <li>Open a portal to the nether</li>
    <li>Have a friend from your old world also open a portal to the nether</li> 
    <li>Have your friend say something, so you can follow his voice and get to the portal leading back to your
      own world. (Keep in mind that everyone else in the nether can hear you, so be very careful doing this)</li>
    <li>Finally to be born again in the old world you were in, you have to sleep in a bed in the old world</li>
  </ol>
