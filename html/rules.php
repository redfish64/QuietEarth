      <h2>Main Concept</h2>
      <p>The Quiet Craft is a minecraft plugin, reachable from within minecraft at thequietcraft.com</p>
      <p>In real life, if you die, you disappear and cannot come back (at least that's seems to be the general understanding). 
	The Quiet Craft is meant to resemble that concept to a degree.
	<b id="warn">If you run out of souls in The Quiet Craft,
	  you instantly be teleported out of your world and into a new one, and you won't (normally) be able to come back.
      </b></p>
      <p>This means that, if you go on a PvP rampage, and kill everyone who visited your world, you will probably
	<b><i>NEVER</i></b> see them again. So as you kill them off, you are making your world a more and more lonely place, until
	finally, there is only you. So have fun in your own empty world, you sealed your fate.</p>
      <p>On the other hand, if you have a griefer that is making everyone angry, maybe you don't <b><i>WANT</i></b> to see them again,
	so feel free to kill them (if you can manage). If you're able to do that, you can rest happily knowing you'll
	not see them anymore.</p>
      <p>But what if everyone seems to be a griefer in your world, then you might want to just end it all and 
	kill yourself. You'll be born in a new world, with, hopefully, new better people to play with, that for
	whatever reason, also didn't like the previous world or were forced out.</p>
      <h2>PvP</h2>
      Killing other players can be fun,  but remember that 
      <b id="warn">everyone you kill will never come back to your world</b>. So in general if you want a fun world 
      it's better to team up with them, become their king and rule them, or have them vote you as president.</b>
<h2>Talking</h2>
When you try saying anything, it's more like real life, in that other people can hear you. They also can hear 
where you are talking from. For example, if you press 'T' and type "Hello there!" after joining, this will
be displayed to other players:
<br>
<blockquote> 
  *direction* &lt;*your name*&gt; shouts "Hello there!"
</blockquote>
For example, if you talk and someone is facing somewhat away from you, they'll get something like
<blockquote> 
  <span id="speech_dir_style">behind left</span> *your name* shouts "Hello there!"
</blockquote>
The direction, "behind left" indicates where the voice is coming from. So, if you want to find the owner of a voice,
you can turn in that direction and look for them. Depending on who is around, 
<span id="warn">it can be dangerous to talk! And by default, you'll shout, attracting people from miles around.</span>

